public class Ludoteca {
    private Lidia lidia;
    private Aisha aisha;
    private Dalsy dalsy;
}
public Ludoteca() {
    lidia = new Lidia();
    aisha = new Aisha();
    dalsy = new Dalsy();
}
public Lidia getLidia() { return lidia; }
public Aisha getAisha() { return aisha; }
public Dalsy getDalsy() { return dalsy; }
